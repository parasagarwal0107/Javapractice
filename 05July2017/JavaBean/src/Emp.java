public class Emp implements java.io.Serializable {
	private int id;
	private String name;

	public Emp() {
	}

	public void setid(int id) {
		this.id = id;
	}

	public int getid() {
		return id;
	}

	public void setname(String name) {
		this.name = name;
	}

	public String getname() {
		return name;
	}
}
